// keyframes // Cada keyframe descreve como o elemento animado deveria se renderizar a um tempo dado durante a sequência de animação. Como a sincronização da animação é definida por um estilo CSS que configura a animação, keyframes usam uma percentage (en-US) para indicar o tempo durante a sequência de animação que eles fazem parte.

//--------------------------------------------

// animations //Animações CSS tornam possível animar transições de um estilo CSS para outro. Animações se consistem de dois componentes: um estilo descrevendo a animação e um set de keyframes que indicam o estado final e inicial do estilo CSS da animação, bem como possíveis waypoints intermediários ao longo do caminho.

//--------------------------------------------
//  animation: pipe-animations 2s infinite linear;
//  2sg é velocidade da animação na tela, 
//infinite é para ele ficar se repetindo em loop e 
//linear para ele ficar na mesma velocidade

